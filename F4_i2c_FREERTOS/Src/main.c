static void CounterThread(void const * argument) {
    uint8_t count = 0;
    char data[2];

    /* We need to send data to the direction
     * register first to setup our led counter as output
     */
    data[0] = 0x00;
    data[1] = 0x00;
    HAL_I2C_Master_Transmit(&hi2c1, (uint16_t) (0x21 << 1), (uint8_t *) data, 2, 1000);

    /* Infinite loop */
    data[0] = 0x14;
    for(;;)
    {
      data[1] = count;
      HAL_I2C_Master_Transmit(&hi2c1, (uint16_t) (0x21 << 1), (uint8_t*) data, 2, 1000);
      count ++;
      osDelay(100);
    }
  }


